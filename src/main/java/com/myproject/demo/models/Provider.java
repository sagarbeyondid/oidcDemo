package com.myproject.demo.models;

public class Provider {

    private String type;
    private String name;

    public Provider() {
    }

    public Provider(String type, String name) {
        this.type = type;
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
